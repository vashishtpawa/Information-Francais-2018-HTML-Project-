<em> Map of Andorra </em> <br /> <br />
<em> Andorra is a tiny country sandwiched between Spain and France, but its national language isn't French nor Spanish, its Catalan, a regional language of Spain, coming from Catalonia, a region
in Spain. Andorra is famous for being high, due to its capital, Andorra la Vella, being the highest capital in Europe elevation-wise, as it has an elevation of about <em class="bold">one</em> kilometre. Andorrra also has a <em class="bold">very</em> 
unique government system. <br /> <br /> Just like Monaco, Andorra is indeed a principality, but unlike Monaco, Andorra has <em class="bold">two</em> princes, those being The Bishop of Urgell, a region in Spain, and The President of France. <br />
This may seem strange now, but back in the medieval era, it was... less... strange? <br /> <br /> Anyways, the simplest way I can put it, back then, Urgell was independent, and France had a king, who were both very Catholic, making Andorra's 
government back then very reasonable.<br /> <br /> Now, however, thanks to Napoleon, sort of, France is a republic with a President, and Urgell is a county of Spain. Andorra couldn't just cease to exist, so it had to make do with what it had. This 
makes Andorra even more interesting, because now, two very different groups of people decide the fate of Andorra. On the French side, the French public votes for the president, meaning the French public is <em class="bold">also</em> deciding one of 
the princes of Andorra, and on Urgell's side, the <em class="bold">Pope, from Vatican City,</em> is appointing the Bishop.<br />

